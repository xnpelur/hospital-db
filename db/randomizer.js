function getRandomInt(end) {
    return Math.floor(Math.random() * end);
}

function getFullName() {
    const male = getRandomInt(2) == 1;

    const lastName = male
        ? maleLastNames[getRandomInt(maleLastNames.length)]
        : femaleLastNames[getRandomInt(femaleLastNames.length)];
    const firstName = male
        ? maleFirstNames[getRandomInt(maleFirstNames.length)]
        : femaleFirstNames[getRandomInt(femaleFirstNames.length)];
    const middleName = male
        ? maleMiddleNames[getRandomInt(maleMiddleNames.length)]
        : femaleMiddleNames[getRandomInt(femaleMiddleNames.length)];

    return `${lastName} ${firstName} ${middleName}`;
}

function getRandomDate(start, end) {
    const startTime = new Date(start).getTime();
    const endTime = new Date(end).getTime();

    const randomTimestamp = startTime + Math.random() * (endTime - startTime);
    const randomDate = new Date(randomTimestamp);

    return randomDate.toISOString().substring(0, 10);
}

function getRandomString(characters, length) {
    const array = [];

    for (let i = 0; i < length; i++) {
        const randomChar = characters.charAt(getRandomInt(characters.length));
        array.push(randomChar);
    }

    return array.join("");
}

function getUsername() {
    const characters = "abcdefghijklmnopqrstuvwxyz";
    const digits = "0123456789";

    const charactersLength = 8 + getRandomInt(5);
    const numberLength = getRandomInt(6);

    return (
        getRandomString(characters, charactersLength) +
        getRandomString(digits, numberLength)
    );
}

function getPassword() {
    const characters =
        "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789!@#$%^&*()-_+=";
    const length = 8 + getRandomInt(5);
    return getRandomString(characters, length);
}

function getPhoneNumber() {
    const first = getRandomInt(1000).toString().padStart(3, "0");
    const second = getRandomInt(1000).toString().padStart(3, "0");
    const third = getRandomInt(100).toString().padStart(2, "0");
    const fourth = getRandomInt(100).toString().padStart(2, "0");

    return `+7 ${first} ${second} ${third} ${fourth}`;
}

